# criar uma fun~cao que armazena dados identificando parametro

def agencia(**carro):
    return carro 


print(agencia(marca= 'Bmw', cor= 'Branco', motor= 1.6, placa = 1234))
print(agencia(marca= 'Gol', cor= 'Branco', motor= 1.6, placa = 1234))
print(agencia(marca= 'Celta', cor= 'Branco'))








